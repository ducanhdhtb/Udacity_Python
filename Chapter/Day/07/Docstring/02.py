def power(base, exponent):
    """
    Calculate the power of a number.

    This function calculates and returns the result of raising the given base to the power of the given exponent.

    Parameters:
    base (int): The base number.
    exponent (int): The exponent.

    Returns:
    int: The result of base raised to the power of exponent.
    """
    return base ** exponent


def greet(name):
    """Greet the person by name."""
    print(f"Hello, {name}!")


# Sử dụng lệnh help để xem docstring
help(power)
help(greet)
